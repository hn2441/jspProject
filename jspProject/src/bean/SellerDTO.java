package bean;

public class SellerDTO {
	private String id;
	private String name;
	private String rrn;
	private String bank;
	private String acc;
	
	String getId() {
		return id;
	}
	void setId(String id) {
		this.id = id;
	}
	String getName() {
		return name;
	}
	void setName(String name) {
		this.name = name;
	}
	String getRrn() {
		return rrn;
	}
	void setRrn(String rrn) {
		this.rrn = rrn;
	}
	String getBank() {
		return bank;
	}
	void setBank(String bank) {
		this.bank = bank;
	}
	String getAcc() {
		return acc;
	}
	void setAcc(String acc) {
		this.acc = acc;
	}
	
	
}
