package bean;

public class BbsDTO {
	private int no;
	private String mid;
	private String sid;
	private String title;
	private String content;
	private String date;
	private int nofveiws;
	
	int getNo() {
		return no;
	}
	void setNo(int no) {
		this.no = no;
	}
	String getMid() {
		return mid;
	}
	void setMid(String mid) {
		this.mid = mid;
	}
	String getSid() {
		return sid;
	}
	void setSid(String sid) {
		this.sid = sid;
	}
	String getTitle() {
		return title;
	}
	void setTitle(String title) {
		this.title = title;
	}
	String getContent() {
		return content;
	}
	void setContent(String content) {
		this.content = content;
	}
	String getDate() {
		return date;
	}
	void setDate(String date) {
		this.date = date;
	}
	int getNofveiws() {
		return nofveiws;
	}
	void setNofveiws(int nofveiws) {
		this.nofveiws = nofveiws;
	}
	

	
	
	
}
